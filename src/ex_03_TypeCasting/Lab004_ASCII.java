package ex_03_TypeCasting;

public class Lab004_ASCII {
    public static void main(String[] args) {

        //Program 6: Character to ASCII
        //Description: Typecasts a character to its ASCII integer value. Example Print A ASCII Int value.
        char a='a';
        char b='b';
        int A=(int)a;
        System.out.println("Ascii value is = "+A);
        int B=(int)b;
        System.out.println("Ascii value is = "+B);
    }
}
