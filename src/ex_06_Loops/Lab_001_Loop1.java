package ex_06_Loops;

public class Lab_001_Loop1 {

    public static void main(String[] args) {

        // For Loop
        // Help you to repeat a block of code.

        // Initialization -> Condition -> Updation(Incre/decre)
        // ICU
        for(int i=0;i<10;i++){
            System.out.println(i);
        }
    }
}
