package ex_007_WhileLoop;

public class Lab_001_whileloop1 {
    public static void main(String[] args) {

        int i = 0;
        while (i < 10) {
            System.out.println(i);
            i++;
        }

        //  I, C, U
        int j = 0;
        for (; j < 10; j++) {
            System.out.println(j);
        }
    }
}
